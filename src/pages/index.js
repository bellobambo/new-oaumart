import Head from "next/head";
import data from "/data.json";
import Navbar from "../../components/Navbar";
import Product from "../../components/Product";
import { useState } from "react";

export default function Home() {
  const [searchQuery, setSearchQuery] = useState("");
  const [currentPage, setCurrentPage] = useState(1);
  const productsPerPage = 5;

  const filteredProducts = data.filter((product) =>
    product.name.toLowerCase().includes(searchQuery.toLowerCase())
  );

  const startIndex = (currentPage - 1) * productsPerPage;
  const endIndex = startIndex + productsPerPage;
  const productsToDisplay = filteredProducts.slice(startIndex, endIndex);

  return (

    <>

      <Navbar />
      <div className="min-h-screen mx-10">
        <Head>
          <title>MarketÂ²</title>
          <meta name="description" content="Generated by create next app" />
          <link rel="icon" href="/favicon64.png" />
        </Head>

        <div className="flex flex-col justify-center mx-auto my-auto mb-10">

          <section className="container flex flex-col justify-center mx-auto my-auto mb-10">
            <h1 className="text-4xl mt-4 text-center">Our Products</h1>
            <div className="mt-4 flex justify-center">
              <input
                className="p-2 outline-none rounded-md w-full max-w-[400px]"
                type="text"
                placeholder="Search by product name"
                onChange={(e) => setSearchQuery(e.target.value)}
              />
            </div>

            <div className="mt-4 gap-4 mx-auto flex flex-col sm:flex-row justify-center w-full sm:w-auto ">
              {productsToDisplay.map((product) => (
                <Product key={product.id} product={product} />
              ))}
            </div>
          </section>

          {/* Pagination controls */}
          <div className="flex justify-center my-6">
            <button
              onClick={() => setCurrentPage(currentPage - 1)}
              disabled={currentPage === 1}
              className="px-4 py-2 mr-2 bg-red-300 cursor-pointer rounded-lg"
            >
              Previous
            </button>
            <button
              onClick={() => setCurrentPage(currentPage + 1)}
              disabled={endIndex >= filteredProducts.length}
              className="px-4 py-2 bg-red-300 cursor-pointer rounded-lg"
            >
              Next
            </button>
          </div>


        </div>
      </div>
    </>
  );
}
